'''
A continuación vamos a ver las formas
de declarar variables de diferentes 
tipos soportados por Python.
'''

nombre = "Ana" # Variable de tipo cadena de texto (str)
edad = 20 # Variable de tipo número entero (int)
estatura = 1.65 # Variable de tipo número decimal (float) 
casado = True # Variable de tipo Booleno (bool)
soltero = False # Idem

print("La variable nombre es de tipo:")
print(type(nombre))

print("La variable edad es de tipo:")
print(type(edad))

print("La variable estatura es de tipo:") 
print(type(estatura))

print("La variable casado es de tipo:")
print(type(casado))

print("La variable soltero es de tipo:")
print(type(soltero))
